// ЧЕСНО: це згенеровані шаблонами приклади, НЕ реальні запити клієнтів. Бізнес
// поки не має розміченого корпусу — і це сама по собі знахідка (див. README):
// класичний ML вимагає даних, яких ще нема. Тут ми імітуємо «що було б, якби
// зібрали ~200 розмічених звернень» — щоб виміряти, чи допоможе це взагалі.
//
// Жодного рядка звідси НЕ використовуємо в held-out тесті (src/eval/heldout.ts).

import type { Sample } from "./classifier";

// Лексика по класах (UA + RU форми, як реально пишуть).
const VOCAB: Record<string, string[]> = {
  fertilizer: [
    "добрива", "селітру", "нітроамофоску", "карбамід", "мінеральні добрива",
    "селитру", "нитроаммофоску", "удобрения", "аміачну селітру",
  ],
  grain: [
    "соняшник", "пшеницю", "ячмінь", "кукурудзу", "горох", "зерно", "насіння",
    "сою", "кукурузу", "зерно пшеницы", "подсолнечник",
  ],
  oilcake: [
    "жмих", "макуху", "соняшниковий жмих", "соєвий шрот", "шрот", "соевый жмых",
  ],
  biofuel: [
    "пелети", "брикети", "тирсу", "біопаливо", "деревні пелети",
    "пеллеты", "опилки", "топливные брикеты",
  ],
  feed: [
    "комбікорм", "комбікорм для птиці", "премікс", "корм для свиней",
    "комбикорм", "комбикорм для бройлеров",
  ],
  quarry: [
    "щебінь", "відсів", "гранітний відсів", "пісок", "інертні матеріали",
    "щебень", "песок", "гранитный отсев",
  ],
  limestone_powder: [
    "вапняк", "вапняк фракції 0-3", "мінеральний порошок", "вапнякову муку",
    "вапняк для розкислення ґрунту", "известняк", "минеральный порошок МП-1",
    "вапняк для металургії",
  ],
};

// Шаблони пакування (для біг-бег класів). {x} = матеріал.
const BAG_TEMPLATES_UA = [
  "потрібні біг-беги під {x}",
  "беги під {x}, тонна",
  "шукаю тару під {x}",
  "потрібен біг-бег для {x}",
  "ціна на біг-бег під {x}",
  "мішки під {x} 1т",
  "тара під {x}, возимо вагонами",
];
const BAG_TEMPLATES_RU = [
  "нужны биг-бэги под {x}",
  "тара под {x}",
  "биг беги для {x}",
  "нужны мешки под {x}",
];

// Шаблони для вапняку/порошку (не про пакування — про сам товар).
const LIME_TEMPLATES = [
  "потрібен {x}",
  "ціна на {x}",
  "{x} оптом",
  "цікавить {x}, які умови",
  "нужен {x}",
  "{x}, какие фракции есть",
];

// Клас "none": загальні/нецільові звернення.
const NONE_SAMPLES = [
  "доброго дня, маєте каталог?",
  "які у вас контакти?",
  "хочу стати вашим дилером",
  "цікавить співпраця, з ким говорити",
  "а ви шлакоблок продаєте?",
  "добрый день, есть прайс?",
  "хочу обговорити партнерство",
  "які умови оплати у вас",
  "доброго ранку, потрібна консультація",
  "здравствуйте, можно узнать про вашу компанию",
];

function isRu(material: string): boolean {
  return /[ыэъ]|селитру|нитро|удобрения|кукурузу|жмых|шрот|пеллеты|опилки|комбикорм|бройлер|щебень|песок|отсев|известняк|минеральн|какие|нужен/.test(
    material,
  );
}

export function generateTrainData(): Sample[] {
  const out: Sample[] = [];

  for (const [label, materials] of Object.entries(VOCAB)) {
    const isLime = label === "limestone_powder";
    for (const m of materials) {
      const templates = isLime
        ? LIME_TEMPLATES
        : isRu(m)
        ? BAG_TEMPLATES_RU
        : BAG_TEMPLATES_UA;
      for (const t of templates) {
        out.push({ text: t.replace("{x}", m), label });
      }
      // Голий матеріал теж як приклад (клієнти часто пишуть одним словом).
      out.push({ text: m, label });
    }
  }

  for (const s of NONE_SAMPLES) out.push({ text: s, label: "none" });

  return out;
}
